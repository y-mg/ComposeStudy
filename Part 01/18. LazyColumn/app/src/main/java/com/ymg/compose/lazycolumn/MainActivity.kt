@Composable
fun LazyColumnEx(itemList: List<ItemData>) {
    LazyColumn {
        /*
        item {
            Item(itemList[0])
        }
        item {
            Item(itemList[0])
        }
        */
        items(itemList) {
            Item(it)
        }
    }


    /**
     * Lazy 키워드는 필요할 때만 보여주겠다는 의미
     *
     * Header 혹은 Footer 가 필요한 경우 직접 LazyColumn 에 아이템을 넣으면 됨
     *
     * 여러 개의 아이템이 반복적으로 나와야 하는 경우에는 items 사용
     */
}

@Preview(showBackground = true)
@Composable
